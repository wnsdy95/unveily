import { mkdir, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const SOURCE_URL = "https://publicsuffix.org/list/public_suffix_list.dat";
const projectRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const outputPath = resolve(projectRoot, "src/publicSuffixRules.js");

const response = await fetch(SOURCE_URL, { redirect: "error" });
if (!response.ok) throw new Error(`PSL download failed: ${response.status}`);
const source = await response.text();
const version = /^\/\/ VERSION:\s*(.+)$/m.exec(source)?.[1]?.trim();
const commit = /^\/\/ COMMIT:\s*([a-f0-9]{40})$/m.exec(source)?.[1];
if (!version || !commit || !source.includes("// ===BEGIN PRIVATE DOMAINS===")) {
  throw new Error("PSL metadata or PRIVATE section is missing");
}

const exact = [];
const wildcard = [];
const exception = [];
for (const rawLine of source.split(/\r?\n/)) {
  const line = rawLine.trim();
  if (!line || line.startsWith("//")) continue;
  const kind = line.startsWith("!") ? "exception" : line.startsWith("*.") ? "wildcard" : "exact";
  const rawDomain = line.replace(/^!|^\*\./, "");
  const asciiDomain = new URL(`https://${rawDomain}`).hostname.toLowerCase().replace(/\.$/, "");
  if (!asciiDomain || asciiDomain.includes("/")) throw new Error(`Invalid PSL rule: ${line}`);
  ({ exact, wildcard, exception })[kind].push(asciiDomain);
}

const uniqueSorted = (values) => Array.from(new Set(values)).sort();
const moduleSource = `// Generated by scripts/update-public-suffix-list.mjs.
// Source: ${SOURCE_URL}
// VERSION: ${version}
// COMMIT: ${commit}
// The embedded list is licensed under the Mozilla Public License 2.0.

export const PUBLIC_SUFFIX_LIST_VERSION = ${JSON.stringify(version)};
export const PUBLIC_SUFFIX_LIST_COMMIT = ${JSON.stringify(commit)};

const EXACT_RULES = new Set(\`${uniqueSorted(exact).join("\n")}\`.split("\\n"));
const WILDCARD_ROOTS = new Set(\`${uniqueSorted(wildcard).join("\n")}\`.split("\\n"));
const EXCEPTION_RULES = new Set(\`${uniqueSorted(exception).join("\n")}\`.split("\\n"));

function normalizedHostname(value) {
  const text = String(value || "").trim().replace(/^\\.+|\\.+$/g, "");
  if (!text) return "";
  try {
    return new URL(\`https://\${text}\`).hostname.toLowerCase().replace(/\\.$/, "");
  } catch {
    return "";
  }
}

function isIpAddress(hostname) {
  return /^\\d{1,3}(?:\\.\\d{1,3}){3}$/.test(hostname) || hostname.includes(":");
}

export function registrableDomain(value) {
  const hostname = normalizedHostname(value);
  if (!hostname || hostname === "localhost" || isIpAddress(hostname)) return hostname;
  const labels = hostname.split(".").filter(Boolean);
  if (labels.length <= 1) return hostname;

  let exactLength = 0;
  let wildcardLength = 0;
  let exceptionLength = 0;
  for (let index = 0; index < labels.length; index += 1) {
    const candidate = labels.slice(index).join(".");
    const candidateLength = labels.length - index;
    if (EXCEPTION_RULES.has(candidate)) exceptionLength = Math.max(exceptionLength, candidateLength);
    if (EXACT_RULES.has(candidate)) exactLength = Math.max(exactLength, candidateLength);
    if (index > 0 && WILDCARD_ROOTS.has(candidate)) {
      wildcardLength = Math.max(wildcardLength, candidateLength + 1);
    }
  }

  if (exceptionLength > 0) {
    const publicSuffixLength = exceptionLength - 1;
    return labels.slice(-Math.min(labels.length, publicSuffixLength + 1)).join(".");
  }
  const publicSuffixLength = Math.max(exactLength, wildcardLength);
  // An unknown TLD or malformed rule set must not merge sibling hosts.
  if (publicSuffixLength === 0) return hostname;
  if (labels.length <= publicSuffixLength) return hostname;
  return labels.slice(-(publicSuffixLength + 1)).join(".");
}
`;

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, moduleSource, "utf8");
console.log(`Updated ${outputPath} from PSL ${version} (${commit})`);
